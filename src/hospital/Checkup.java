    private void cst1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cst1KeyTyped
        char c = evt.getKeyChar();

        if (c == 43 && c != 8 && c != 43) {
            evt.consume();
        }
        if (!Character.isDigit(c) && c != 8 && c != 43) {
            evt.consume();
        }

    }//GEN-LAST:event_cst1KeyTyped

