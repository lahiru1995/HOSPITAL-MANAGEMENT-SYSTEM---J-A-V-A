    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String checkup = check.getText();
            String cst = cost.getText();
            if (checkup.equals("") || cst.equals("")) {
                JOptionPane.showMessageDialog(null, "Pls fill the form");
            } else {
                PreparedStatement ps = db.con().prepareStatement("insert into checkups values('" + checkup + "','" + cst + "')");
                ps.execute();
                JOptionPane.showMessageDialog(null, "Success!!");
                check.setText("");
                cost.setText("");
                try {
                    jComboBox1.removeAllItems();
                    PreparedStatement ps1 = db.con().prepareStatement("select name from checkups");
                    ResultSet rs = ps1.executeQuery();

                    while (rs.next()) {
                        jComboBox1.addItem(rs.getString(1));
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Checkup.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Checkup.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String checkup = jComboBox1.getSelectedItem().toString();
        String cst = cst1.getText();
        if (cst.equals("")) {
            JOptionPane.showMessageDialog(null, "Pls fill the form");
        } else {
            try {
                PreparedStatement ps = db.con().prepareStatement("UPDATE `checkups` SET `cost`='" + cst + "' WHERE `name`='" + checkup + "'");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Update Success!!");
                cst1.setText("");
                jComboBox1.setSelectedIndex(0);
            } catch (SQLException ex) {
                Logger.getLogger(Checkup.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        cst1.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

