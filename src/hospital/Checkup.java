    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (!(jComboBox1.getSelectedIndex() == -1)) {
            try {
                PreparedStatement ps = db.con().prepareStatement("select cost from checkups where name='" + jComboBox1.getSelectedItem().toString() + "'");
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    cst1.setText(rs.getString(1));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Checkup.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

