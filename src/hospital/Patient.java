    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        PreparedStatement pst;
        ResultSet rs;

        if (search.getText().equals("")) {
            settable();
        }
        try {
            pst = db.con().prepareStatement("SELECT `patid`, `first_name`, `last_name`,`gender`, `telephone` FROM `patient` WHERE `first_name` like'%" + search.getText() + "%' OR patid like'%" + search.getText() + "%'");

            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_searchKeyReleased

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            TableModel model1 = jTable1.getModel();

            PreparedStatement pst;
            ResultSet rs;

            pst = db.con().prepareStatement("SELECT * FROM `patient` where patid='" + model1.getValueAt(jTable1.getSelectedRow(), 0) + "'");

            rs = pst.executeQuery();

            while (rs.next()) {
                pIdField.setText(rs.getString(1));
                pNameField.setText(rs.getString(2));
                pNameField1.setText(rs.getString(3));
                pAgeField.setText(rs.getString(4));
                if (rs.getString(5).equals("male")) {
                    male.setSelected(true);
                    fmale.setSelected(false);
                } else {
                    fmale.setSelected(true);
                    male.setSelected(false);
                }
                pPhoneField.setText(rs.getString(7));
                pAddressField.setText(rs.getString(8));
                jTextField2.setText(rs.getString(6));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jTable1MouseClicked

