    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            PreparedStatement prp;

            String date = new SimpleDateFormat("yyyy-MM-dd").format(Calendar.getInstance().getTime());
            String id = pIdField.getText();
            String name = pNameField.getText();
            String lname = pNameField1.getText();

            String gender;
            if (male.isSelected()) {
                gender = "male";
            } else {
                gender = "female";
            }
            String address = pAddressField.getText();
            String phone = pPhoneField.getText();

            String sql = "INSERT INTO `patient`(`patid`, `first_name`, `last_name`, `age`, `gender`, `date`, `telephone`, `address`)  values (?,?,?,?,?,?,?,?)";

            if (pAgeField.equals("") || name.equals("") || lname.equals("") || gender.equals("") || address.equals("") || phone.equals("")) {
                JOptionPane.showMessageDialog(null, "Pls fill the form!");
            } else {
                int age = Integer.parseInt(pAgeField.getText());
                prp = db.con().prepareStatement(sql);
                prp.setString(1, id);
                prp.setString(2, name);
                prp.setString(3, lname);
                prp.setInt(4, age);
                prp.setString(5, gender);
                prp.setString(6, date);
                prp.setString(7, phone);
                prp.setString(8, address);
                prp.execute();
                JOptionPane.showMessageDialog(null, "Data Saved");

                pNameField.setText("");
                pNameField1.setText("");
                pAgeField.setText("");

                pPhoneField.setText("");
                pAddressField.setText("");
                male.setSelected(false);
                fmale.setSelected(false);

                setId();
                settable();
                settable2();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

