    public void setavailable() {
        try {
            PreparedStatement ps = db.con().prepareStatement("select totalPatients,assigned from doctor where clinic='" + clinic.getSelectedItem().toString() + "'");
            ResultSet rs = ps.executeQuery();

            int x = 0, y = 0;

            while (rs.next()) {
                x = rs.getInt(1);
                y = rs.getInt(2);
            }

            if (x > y) {
                jTextField1.setText(String.valueOf(x - y));
                jButton1.setEnabled(true);
            } else {
                jTextField1.setText("0");
                jButton1.setEnabled(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
