    private void pAgeFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pAgeFieldKeyTyped
        char c = evt.getKeyChar();

        if (c == 43) {
            evt.consume();
        }
        if (!Character.isDigit(c) && c != 8 && c != 43) {
            evt.consume();
        }

    }//GEN-LAST:event_pAgeFieldKeyTyped

    private void pPhoneFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pPhoneFieldKeyTyped
        char c = evt.getKeyChar();

        String text = pPhoneField.getText();

        if (c == 43 && text.indexOf('+') != -1) {
            evt.consume();
        }
        if (!Character.isDigit(c) && c != 8 && c != 43) {
            evt.consume();
        }

        if (text.length() > 9) {
            evt.consume();
        }
    }//GEN-LAST:event_pPhoneFieldKeyTyped

