    public void setId() {
        try {
            PreparedStatement ps = db.con().prepareStatement("select count(patid) from patient");
            ResultSet rs = ps.executeQuery();
            int id = 0;
            while (rs.next()) {
                id = Integer.parseInt(rs.getString(1));
            }
            id = id + 1;
            pIdField.setText("p" + String.valueOf(id));
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

